package ViewsModels.SanPhamAndChiTietSP;

import java.math.BigDecimal;

/**
 *
 * @author MINH DUC
 */
public class HDCTRepo {
    private  String maHD,maSP;
    private int soLuong;
    private BigDecimal donGia, thanhTien;

    public HDCTRepo() {
    }

    public HDCTRepo(String maHD, String maSP, int soLuong, BigDecimal donGia, BigDecimal thanhTien) {
        this.maHD = maHD;
        this.maSP = maSP;
        this.soLuong = soLuong;
        this.donGia = donGia;
        this.thanhTien = thanhTien;
    }

    public String getMaHD() {
        return maHD;
    }

    public void setMaHD(String maHD) {
        this.maHD = maHD;
    }

    public String getMaSP() {
        return maSP;
    }

    public void setMaSP(String maSP) {
        this.maSP = maSP;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public BigDecimal getDonGia() {
        return donGia;
    }

    public void setDonGia(BigDecimal donGia) {
        this.donGia = donGia;
    }

    public BigDecimal getThanhTien() {
        return thanhTien;
    }

    public void setThanhTien(BigDecimal thanhTien) {
        this.thanhTien = thanhTien;
    }
    
    
}
