    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ma = this.txtMa.getText();
        String ten = this.txtTen.getText();

        for (MauSac mausac : this.ISerSP.getListMS()) {
            if (mausac.getMa().equals(ma)) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại!");
                return;
            }
        }
        MauSac ms = new MauSac();
        ms.setMa(ma);
        ms.setTen(ten);

        JOptionPane.showMessageDialog(this, this.ISerSP.themMauSac(ms));
        addTable(this.ISerSP.getListMS());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String ma = this.txtMa.getText();
        String ten = this.txtTen.getText();
        for (MauSac mausac : this.ISerSP.getListMS()) {
            if (mausac.getMa().equals(ma)) {
                MauSac ms = new MauSac();
                ms.setMa(ma);
                ms.setTen(ten);

                JOptionPane.showMessageDialog(this, this.ISerSP.updateMauSac(ms));
                addTable(this.ISerSP.getListMS());
                return;
            }
        }

        JOptionPane.showMessageDialog(this, "Mã không tồn tại!");

    }//GEN-LAST:event_btnUpdateActionPerformed

