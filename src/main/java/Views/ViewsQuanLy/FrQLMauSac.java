    private void addTable(List<MauSac> list) {
        DefaultTableModel model = (DefaultTableModel) tblColors.getModel();
        model.setRowCount(0);
        int i = 0;
        for (MauSac ms : list) {
            i++;
            model.addRow(new Object[]{i, ms.getMa(), ms.getTen()});
        }
    }
